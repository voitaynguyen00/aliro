#include <gtest/gtest.h>

#include "aliro/crypto/openSslCryptoProvider.h"

using namespace aliro;

class HkdfTest : public ::testing::Test {
protected:
    OpenSslCryptoProvider mCrypto;
};

// RFC 5869 Test Case 1 — SHA-256
// IKM=22×0x0b, Salt=000102...0c (13 bytes), Info=f0f1...f9 (10 bytes), L=42
TEST_F(HkdfTest, rfc5869_testCase1) {
    Bytes ikm = {0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b,
                 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b};
    Bytes salt = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c};
    Bytes info = {0xf0, 0xf1, 0xf2, 0xf3, 0xf4, 0xf5, 0xf6, 0xf7, 0xf8, 0xf9};
    Bytes expectedOkm = {0x3c, 0xb2, 0x5f, 0x25, 0xfa, 0xac, 0xd5, 0x7a, 0x90, 0x43, 0x4f,
                         0x64, 0xd0, 0x36, 0x2f, 0x2a, 0x2d, 0x2d, 0x0a, 0x90, 0xcf, 0x1a,
                         0x5a, 0x4c, 0x5d, 0xb0, 0x2d, 0x56, 0xec, 0xc4, 0xc5, 0xbf, 0x34,
                         0x00, 0x72, 0x08, 0xd5, 0xb8, 0x87, 0x18, 0x58, 0x65};

    auto result = mCrypto.hkdfDerive(ikm, salt, info, 42);
    ASSERT_TRUE(result.has_value());
    EXPECT_EQ(*result, expectedOkm);
}

TEST_F(HkdfTest, deterministicOutput) {
    Bytes ikm = {0xAA, 0xBB, 0xCC};
    Bytes salt = {0xDD, 0xEE};
    Bytes info = {0xFF};

    auto r1 = mCrypto.hkdfDerive(ikm, salt, info, 16);
    auto r2 = mCrypto.hkdfDerive(ikm, salt, info, 16);
    ASSERT_TRUE(r1.has_value() && r2.has_value());
    EXPECT_EQ(*r1, *r2);
}

TEST_F(HkdfTest, outputLength_respected) {
    Bytes ikm = {0x01, 0x02};
    Bytes salt;
    Bytes info;

    auto r16 = mCrypto.hkdfDerive(ikm, salt, info, 16);
    auto r32 = mCrypto.hkdfDerive(ikm, salt, info, 32);
    ASSERT_TRUE(r16.has_value() && r32.has_value());
    EXPECT_EQ(r16->size(), 16u);
    EXPECT_EQ(r32->size(), 32u);
}
